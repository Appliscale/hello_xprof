-module(hello_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1, hello/0, say_hello/0]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    hello_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

%%====================================================================
%% Internal functions
%%====================================================================

hello() ->
    io:format("~s~n", ["Hello, world!"]),
    timer:sleep(100).

say_hello() ->
    lists:foreach(
        fun(X) ->
            hello()
        end,
        lists:seq(1, 600)).